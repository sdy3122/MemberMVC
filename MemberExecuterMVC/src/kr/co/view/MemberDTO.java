package kr.co.view;

class MemberDTO {
	String id;
	String pw;
	String address;
	String phone;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getAd() {
		return address;
	}
	public void setAd(String address) {
		this.address = address;
	}
	public String getPn() {
		return phone;
	}
	public void setPn(String phone) {
		this.phone = phone;
	}
}